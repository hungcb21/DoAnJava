    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
              try {
            if(txtDiaChi.getText().isEmpty()||txtLuong.getText().isEmpty()||txtMaNV.getText().isEmpty()
                 )
            {
           JOptionPane.showMessageDialog(null, "Vui lòng nhập đủ thông tin");
        }
           else
            {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String connectionUrl = "jdbc:sqlserver://DESKTOP-QEN4LJI;databaseName=QLTHUEXE;user=sa;password=12";
            Connection con = DriverManager.getConnection(connectionUrl);
            String query  ="INSERT into NV(MANV,TENNV,DIACHI,SDT,GT,LUONG) values (?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareCall(query);
             pst.setString(1, txtMaNV.getText().toUpperCase());
             pst.setString(2, txtTenNV.getText());
             pst.setString(3, txtDiaChi.getText());
             pst.setString(4, txtSdt.getText());
               if(btnNam.isSelected())
            {
                gt="Nam";
            }
            
             if(btnNu.isSelected())
            {
                gt="Nữ";
            }
            pst.setString(5, gt);
            pst.setString(6, txtLuong.getText());
            txtDiaChi.setText("");
            txtLuong.setText("");
            txtMaNV.setText("");
            txtSdt.setText("");
            txtTenNV.setText("");
            pst.execute();
            JOptionPane.showMessageDialog(null, "Thêm nhân viên thành công");
            DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
            model.setRowCount(0);
            show_NV();
            }
        } catch (Exception e) {
          JOptionPane.showMessageDialog(null, "Thêm nhân viên không thành công");
        }
    }//GEN-LAST:event_btnThemActionPerformed

