    public ArrayList<NhanVien> nvList(){
        ArrayList<NhanVien> nvList = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String connectionUrl = "jdbc:sqlserver://DESKTOP-QEN4LJI;databaseName=QLTHUEXE;user=sa;password=12";
            Connection con = DriverManager.getConnection(connectionUrl);
            String query ="SELECT * FROM NV";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            NhanVien nv;
            while(rs.next()){
                nv= new NhanVien(rs.getString("MANV"),rs.getString("TENNV"),rs.getString("DIACHI"),rs.getString("SDT")
                ,rs.getString("GT"),rs.getString("LUONG"));
                nvList.add(nv);
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }  
        return nvList;
    }
      
           public final void show_NV(){
        ArrayList<NhanVien> list = nvList();
        DefaultTableModel model = (DefaultTableModel)tblNhanVien.getModel();
        Object[] row = new Object[6];
        for(int i =0;i<list.size();i++)
        {
            row[0] = list.get(i).getMaNV();
            row[1] = list.get(i).getTenNV();
            row[2] = list.get(i).getGT();
            row[3] = list.get(i).getSDT();
            row[4] = list.get(i).getDiaChi();
            row[5] = list.get(i).getLUONG();
            model.addRow(row);
        }
    }

